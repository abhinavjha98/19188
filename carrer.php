<p align="center"><b><u><h3>Planning your Future</h3></u></b></p><br>
If you are a high school student then your entire future is lying in front of you. It is you with your parents who need to make right decision about your career from now on.
It is essential that for a bright future you must start planning from now on. And for right planning you need right guidance.
Right career guidance can tell you, about the career options that could suit you the most. Hence, you can start preparing for that option from the beginning.
